    private void btprocurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btprocurarActionPerformed
        try {
            ps=conn.prepareStatement("select * from clientes where numquarto=? and estado=?");
            ps.setString(1, txtnumquarto.getText());
            ps.setString(2, "NULL");
            rs=ps.executeQuery();
            while(rs.next()){
                txtnome.setText(rs.getString("nome"));
                txttelefone.setText(rs.getString("telefone"));
                txtbi.setText(rs.getString("id"));
                txtprecod.setText(rs.getString("preco"));
                txtdatae.setText(rs.getString("dataentrada"));
                
                String s1,s2;
                s1=txtdatasa.getText();
                s2=rs.getString("dataentrada");
                SimpleDateFormat dtf=new SimpleDateFormat("yyyy-MM-dd");
                
                Date d1=dtf.parse(s1);
                Date d2=dtf.parse(s2);
                
                long dif=d1.getTime()-d2.getTime();
                int dias=(int)dif/(24*60*60*1000);
                int d=dias+1;

                    txttdia.setText(String.valueOf(d));
                    double preco=Double.parseDouble(rs.getString("preco"))*d;
                    txttconta.setText(String.valueOf(preco));
                
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro em: "+e.getMessage());
        }
    }//GEN-LAST:event_btprocurarActionPerformed

