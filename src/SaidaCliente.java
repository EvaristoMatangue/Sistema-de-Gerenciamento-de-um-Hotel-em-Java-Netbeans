    public void MostrarDataNaTabela(){
        try {
            ps=conn.prepareStatement("select * from clientes where estado=?");
            ps.setString(1, "null");
            rs=ps.executeQuery();
            ResultSetMetaData ob=rs.getMetaData();
            int n=ob.getColumnCount();
            DefaultTableModel tb= (DefaultTableModel) tabela.getModel();
            tb.setRowCount(0);
            while(rs.next()){
                Vector v=new Vector();
                for(int i=1;i<=n;i++){
                    v.add(rs.getString("nome"));
                    v.add(rs.getString("telefone"));
                    v.add(rs.getString("id"));
                    v.add(rs.getString("numquarto"));
                    v.add(rs.getString("cama"));
                    v.add(rs.getString("tipoq"));
                    v.add(rs.getString("dataentrada"));
                    v.add(rs.getString("preco"));
                    v.add(rs.getString("nacionalidade"));
                }
                tb.addRow(v);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
