    public void ColocarnaTabela(){
        int c=0;
        
        try {
            ps=conn.prepareStatement("select * from quartos");
            rs=ps.executeQuery();
            
            ResultSetMetaData rsmd= rs.getMetaData();
            c=rsmd.getColumnCount();
            DefaultTableModel tb= (DefaultTableModel) tabela.getModel();
            tb.setRowCount(0);
            
            while(rs.next()){
                Vector coluna= new Vector();
                
                for(int i=0;i<=c;i++){
                    coluna.add(rs.getString("numquarto"));
                    coluna.add(rs.getString("tipoq"));
                    coluna.add(rs.getString("cama"));
                    coluna.add(rs.getString("preco"));
                    coluna.add(rs.getString("estado"));
                }
                tb.addRow(coluna);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
