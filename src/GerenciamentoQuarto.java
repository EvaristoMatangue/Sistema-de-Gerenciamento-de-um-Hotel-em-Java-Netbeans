    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        if(txtnumquarto.getText().equals("")){
            JOptionPane.showMessageDialog(null, "E Obrigatorio Preecher todos os Campos!");
            txtnumquarto.requestFocus();
            
        }
       
       else if(txtpreco.getText().equals("")){
            JOptionPane.showMessageDialog(null, "E Obrigatorio Preecher todos os Campos!");
            txtpreco.requestFocus();
        }
       else{
            try {
                ps=conn.prepareStatement("select * from quartos where numquarto=?");
                ps.setString(1, txtnumquarto.getText());
                rs=ps.executeQuery();
                
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "O Quarto ja esta ocupado, tenta outro");
                }else{
                    ps=conn.prepareStatement("insert into quartos(numquarto,tipoq,cama,preco,estado) values(?,?,?,?,?)");
                    ps.setString(1, txtnumquarto.getText());
                    ps.setString(2, cmbq.getItemAt(cmbq.getSelectedIndex()));
                    ps.setString(3, cmbcama.getItemAt(cmbcama.getSelectedIndex()));
                    ps.setString(4, txtpreco.getText());
                    ps.setString(5, "Nao Reservado");
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Adicionado com Sucesso");
                    txtnumquarto.setText("");
                    txtpreco.setText("");
                    ColocarnaTabela();
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "erro na insercao em: "+e.getMessage());
            }
           
       }
        
    }//GEN-LAST:event_btAdicionarActionPerformed

